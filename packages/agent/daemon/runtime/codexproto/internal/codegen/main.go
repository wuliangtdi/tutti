func normalizeGeneratedHeader(src []byte, codexCommit string) []byte {
	if len(src) == 0 {
		return src
	}
	parts := strings.SplitN(string(src), "\n", 2)
	if len(parts) == 0 {
		return src
	}
	header := strings.TrimSpace(parts[0])
	headerLower := strings.ToLower(header)
	if !strings.HasPrefix(headerLower, "// code generated") && !strings.HasPrefix(headerLower, "// generated") {
		return src
	}
	rest := ""
	if len(parts) > 1 {
		rest = strings.TrimLeft(parts[1], "\n")
	}
	for strings.Contains(rest, "\n\n\n") {
		rest = strings.ReplaceAll(rest, "\n\n\n", "\n\n")
	}
	return []byte(generatedHeader(codexCommit) + rest)
}

func generatedHeader(codexCommit string) string {
	return generatedHeaderBase + "// Source codex commit: " + codexCommit + "\n\n"
}

func writeProtocolMetadata(outDir, codexCommit, codexVersion string) error {
	var b strings.Builder
	b.WriteString(generatedHeader(codexCommit))
	b.WriteString("package codexproto\n\n")
	b.WriteString("// GeneratedCodexCommit is the Codex source commit used to generate this protocol package.\n")
	fmt.Fprintf(&b, "const GeneratedCodexCommit = %q\n\n", codexCommit)
	b.WriteString("// GeneratedCodexVersion is the Codex package version used to generate this protocol package.\n")
	fmt.Fprintf(&b, "const GeneratedCodexVersion = %q\n", codexVersion)
	return writeFile(outDir, "metadata_gen.go", []byte(b.String()))
}

func writeFile(dir, name string, contents []byte) error {
	path := filepath.Join(dir, name)
	if filepath.Ext(name) == ".go" {
		formatted, err := format.Source(contents)
		if err != nil {
			return fmt.Errorf("format %s: %w", name, err)
		}
		contents = formatted
	}
	return os.WriteFile(path, contents, 0o644)
}

func findSchemaFiles(schemaDir string) ([]string, error) {
	var files []string
	err := filepath.WalkDir(schemaDir, func(path string, d os.DirEntry, err error) error {
		if err != nil {
			return err
		}
		if d.IsDir() {
			return nil
		}
		if filepath.Ext(path) != ".json" {
			return nil
		}
		files = append(files, path)
		return nil
	})
	if err != nil {
		return nil, err
	}
	sort.Strings(files)
	return files, nil
}

func safeDoFile(gen *generator.Generator, file string) (err error) {
	sanitized, cleanup, err := sanitizeSchemaFile(file)
	if err != nil {
		return err
	}
	defer cleanup()
	defer func() {
		if r := recover(); r != nil {
			err = fmt.Errorf("panic: %v", r)
		}
	}()
	return gen.DoFile(sanitized)
}

func sanitizeSchemaFile(path string) (string, func(), error) {
	data, err := os.ReadFile(path)
	if err != nil {
		return "", nil, err
	}
	var node any
	if err := json.Unmarshal(data, &node); err != nil {
		return "", nil, err
	}
	stripSubschemas(node)
	out, err := json.Marshal(node)
	if err != nil {
		return "", nil, err
	}
	tempDir, err := os.MkdirTemp("", "tutti-codexproto-sanitized-*")
	if err != nil {
		return "", nil, err
	}
	sanitizedName := "sanitized-" + filepath.Base(path)
	sanitizedPath := filepath.Join(tempDir, sanitizedName)
	if err := os.WriteFile(sanitizedPath, out, 0o644); err != nil {
		_ = os.RemoveAll(tempDir)
		return "", nil, err
	}
	cleanup := func() {
		_ = os.RemoveAll(tempDir)
	}
	return sanitizedPath, cleanup, nil
}

func stripSubschemas(node any) {
	switch value := node.(type) {
	case map[string]any:
		delete(value, "oneOf")
		delete(value, "anyOf")
		for _, child := range value {
			stripSubschemas(child)
		}
	case []any:
		for _, child := range value {
			stripSubschemas(child)
		}
	}
}

func repoRoot() (string, error) {
	cwd, err := os.Getwd()
	if err != nil {
		return "", err
	}
	if packageRoot := filepath.Join(cwd, "runtime", "codexproto"); isPackageRoot(packageRoot) {
		return packageRoot, nil
	}

	// Walk up until we find the codexproto package root.
	current := cwd
	for {
		if isPackageRoot(current) {
			return current, nil
		}
		parent := filepath.Dir(current)
		if parent == current {
			break
		}
		current = parent
	}

	return "", fmt.Errorf("unable to locate codexproto package root from %s", cwd)
}

func isPackageRoot(path string) bool {
	return exists(filepath.Join(path, "schema", "json", schemaBundleFile)) &&
		exists(filepath.Join(path, "internal", "codegen", "main.go"))
}

func exists(path string) bool {
	_, err := os.Stat(path)
	return err == nil
}


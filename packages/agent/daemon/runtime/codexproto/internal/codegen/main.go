const (
	schemaBundleFile    = "codex_app_server_protocol.schemas.json"
	generatedHeaderBase = "// Code generated by runtime/codexproto/internal/codegen. DO NOT EDIT.\n"
	codexSourceCommit   = "6d2168f06ae275d5e1f73cabf935d2bcc8549998"
	codexSourceVersion  = "0.0.0"
)

func main() {
	packageRoot, err := repoRoot()
	if err != nil {
		fatal(err)
	}

	schemaDir := filepath.Join(packageRoot, "schema", "json")
	if err := generateProtocolTypes(schemaDir, packageRoot, codexSourceCommit, codexSourceVersion); err != nil {
		fatal(err)
	}

	if err := generateRPCStubs(schemaDir, packageRoot, codexSourceCommit); err != nil {
		fatal(err)
	}
}

func generateProtocolTypes(schemaDir, repoRoot, codexCommit, codexVersion string) error {
	cfg := generator.Config{
		DefaultPackageName: "codexproto",
		DefaultOutputName:  "types_gen.go",
		Tags:               []string{"json"},
		Capitalizations:    []string{"ID", "JSON", "RPC", "MCP", "HTTP", "URL", "SSE", "UUID"},
		OnlyModels:         true,
	}
	gen, err := generator.New(cfg)
	if err != nil {
		return err
	}

	schemaFiles, err := findSchemaFiles(schemaDir)
	if err != nil {
		return err
	}

	skip := skipSchemaFiles()
	var fallbacks []string
	var titles []string
	for _, file := range schemaFiles {
		if filepath.Base(file) == schemaBundleFile {
			continue
		}
		if skip[filepath.Base(file)] {
			continue
		}
		title, titleErr := schemaTitle(file)
		if titleErr != nil {
			return titleErr
		}
		if title != "" {
			titles = append(titles, title)
		}
		if err := safeDoFile(gen, file); err != nil {
			if title == "" {
				return fmt.Errorf("generate types from %s: %w", file, err)
			}
			fallbacks = append(fallbacks, title)
		}
	}

	sources, err := gen.Sources()
	if err != nil {
		return err
	}

	manualTypes := manualProtocolTypes()
	for name, src := range sources {
		sources[name] = filterManualTypeDeclarations(src, manualTypes)
	}

	outDir := repoRoot

	generated := collectGeneratedTypes(sources)
	for name := range manualTypes {
		generated[name] = struct{}{}
	}
	for _, title := range titles {
		if title == "" {
			continue
		}
		if _, ok := generated[title]; ok {
			continue
		}
		if _, ok := generated[title+"JSON"]; ok {
			continue
		}
		fallbacks = append(fallbacks, title)
	}

	for name, src := range sources {
		normalized := normalizeGeneratedHeader(src, codexCommit)
		if err := os.WriteFile(filepath.Join(outDir, name), normalized, 0o644); err != nil {
			return err
		}
	}

	if err := writeFallbackTypes(outDir, fallbacks, generated, codexCommit); err != nil {
		return err
	}

	if err := writeProtocolAliases(outDir, generated, fallbacks, codexCommit); err != nil {
		return err
	}

	return writeProtocolMetadata(outDir, codexCommit, codexVersion)
}

func filterManualTypeDeclarations(src []byte, manualTypes map[string]struct{}) []byte {
	lines := strings.SplitAfter(string(src), "\n")
	out := make([]string, 0, len(lines))
	for i := 0; i < len(lines); {
		line := lines[i]
		trimmed := strings.TrimSpace(line)
		if strings.HasPrefix(trimmed, "type ") {
			fields := strings.Fields(trimmed)
			if len(fields) >= 2 {
				if _, ok := manualTypes[fields[1]]; ok {
					i++
					balance := strings.Count(line, "{") - strings.Count(line, "}")
					for i < len(lines) && balance > 0 {
						balance += strings.Count(lines[i], "{") - strings.Count(lines[i], "}")
						i++
					}
					continue
				}
			}
		}
		out = append(out, line)
		i++
	}
	return []byte(strings.Join(out, ""))
}

type schemaVariant struct {
	Properties map[string]schemaProperty `json:"properties"`
}

type schemaProperty struct {
	Enum  []string `json:"enum"`
	Ref   string   `json:"$ref"`
	Type  string   `json:"type"`
	Items *struct {
		Ref string `json:"$ref"`
	} `json:"items"`
}

type oneOfSchema struct {
	OneOf []schemaVariant `json:"oneOf"`
}

func generateRPCStubs(schemaDir, repoRoot, codexCommit string) error {
	defs, err := parseDefinitionNames(schemaDir)
	if err != nil {
		return err
	}

	clientMethods, err := loadMethods(filepath.Join(schemaDir, "ClientRequest.json"))
	if err != nil {
		return err
	}
	clientMethods = append(clientMethods, manualClientMethods()...)
	sort.Slice(clientMethods, func(i, j int) bool { return clientMethods[i].Method < clientMethods[j].Method })

	serverMethods, err := loadMethods(filepath.Join(schemaDir, "ServerRequest.json"))
	if err != nil {
		return err
	}

	notifications, err := loadNotifications(filepath.Join(schemaDir, "ServerNotification.json"))
	if err != nil {
		return err
	}

	clientRPC, err := buildRPCMethods(clientMethods, defs, clientResponseOverrides())
	if err != nil {
		return err
	}

	serverRPC, err := buildRPCMethods(serverMethods, defs, nil)
	if err != nil {
		return err
	}

	outDir := repoRoot

	if err := writeFile(outDir, "client_requests_gen.go", renderClientRequests(clientRPC, codexCommit)); err != nil {
		return err
	}

	if err := writeFile(outDir, "server_requests_gen.go", renderServerRequests(serverRPC, codexCommit)); err != nil {
		return err
	}

	return writeFile(outDir, "notifications_gen.go", renderNotifications(notifications, codexCommit))
}

func parseDefinitionNames(schemaDir string) (map[string]struct{}, error) {
	files, err := findSchemaFiles(schemaDir)
	if err != nil {
		return nil, err
	}
	defs := make(map[string]struct{})
	for _, file := range files {
		data, err := os.ReadFile(file)
		if err != nil {
			return nil, err
		}
		var doc struct {
			Title       string                     `json:"title"`
			Definitions map[string]json.RawMessage `json:"definitions"`
		}
		if err := json.Unmarshal(data, &doc); err != nil {
			return nil, err
		}
		if doc.Title != "" {
			defs[doc.Title] = struct{}{}
		}
		for name := range doc.Definitions {
			defs[name] = struct{}{}
		}
	}
	return defs, nil
}

func schemaTitle(path string) (string, error) {
	data, err := os.ReadFile(path)
	if err != nil {
		return "", err
	}
	var doc struct {
		Title string `json:"title"`
	}
	if err := json.Unmarshal(data, &doc); err != nil {
		return "", err
	}
	return doc.Title, nil
}

func writeFallbackTypes(outDir string, titles []string, generated map[string]struct{}, codexCommit string) error {
	fallbackPath := filepath.Join(outDir, "fallback_gen.go")
	if len(titles) == 0 {
		_ = os.Remove(fallbackPath)
		return nil
	}

	unique := map[string]struct{}{}
	for _, title := range titles {
		if title != "" {
			unique[title] = struct{}{}
		}
	}

	aliases := map[string]string{}
	var names []string
	for name := range unique {
		if _, exists := generated[name]; exists {
			continue
		}
		if target, ok := sanitizedFallbackTarget(name, generated); ok {
			aliases[name] = target
			continue
		}
		names = append(names, name)
	}
	if len(names) == 0 && len(aliases) == 0 {
		_ = os.Remove(fallbackPath)
		return nil
	}
	sort.Strings(names)
	aliasNames := make([]string, 0, len(aliases))
	for name := range aliases {
		aliasNames = append(aliasNames, name)
	}
	sort.Strings(aliasNames)

	var b strings.Builder
	b.WriteString(generatedHeader(codexCommit))
	b.WriteString("package codexproto\n\n")
	b.WriteString("// Fallback types for schemas that failed to generate.\n")
	for _, name := range aliasNames {
		b.WriteString("type ")
		b.WriteString(name)
		b.WriteString(" = ")
		b.WriteString(aliases[name])
		b.WriteString("\n")
	}
	for _, name := range names {
		b.WriteString("type ")
		b.WriteString(name)
		b.WriteString(" interface{}\n")
	}

	return os.WriteFile(fallbackPath, []byte(b.String()), 0o644)
}

func sanitizedFallbackTarget(name string, generated map[string]struct{}) (string, bool) {
	target := "Sanitized" + name + "JSON"
	if _, ok := generated[target]; ok {
		return target, true
	}
	return "", false
}

func collectGeneratedTypes(sources map[string][]byte) map[string]struct{} {
	generated := map[string]struct{}{}
	for _, src := range sources {
		lines := strings.Split(string(src), "\n")
		for _, line := range lines {
			line = strings.TrimSpace(line)
			if strings.HasPrefix(line, "type ") {
				fields := strings.Fields(line)
				if len(fields) >= 2 {
					generated[fields[1]] = struct{}{}
				}
			}
		}
	}
	return generated
}

func writeProtocolAliases(outDir string, generated map[string]struct{}, fallbacks []string, codexCommit string) error {
	existing := map[string]struct{}{}
	for name := range generated {
		existing[name] = struct{}{}
	}
	for _, name := range fallbacks {
		if name != "" {
			existing[name] = struct{}{}
		}
	}

	aliases := map[string]string{}
	for name := range generated {
		if !strings.HasSuffix(name, "JSON") {
			continue
		}
		base := strings.TrimSuffix(name, "JSON")
		if base == "" {
			continue
		}
		if _, ok := existing[base]; ok {
			continue
		}
		aliases[base] = name
	}

	aliasPath := filepath.Join(outDir, "aliases_gen.go")
	if len(aliases) == 0 {
		_ = os.Remove(aliasPath)
		return nil
	}

	names := make([]string, 0, len(aliases))
	for name := range aliases {
		names = append(names, name)
	}
	sort.Strings(names)

	var b strings.Builder
	b.WriteString(generatedHeader(codexCommit))
	b.WriteString("package codexproto\n\n")
	b.WriteString("// Aliases for JSON-suffixed schema types.\n")
	for _, name := range names {
		b.WriteString("type ")
		b.WriteString(name)
		b.WriteString(" = ")
		b.WriteString(aliases[name])
		b.WriteString("\n")
	}

	return os.WriteFile(aliasPath, []byte(b.String()), 0o644)
}


func renderClientRequests(methods []rpcMethod, codexCommit string) []byte {
	var b strings.Builder
	b.WriteString(generatedHeader(codexCommit))
	b.WriteString("package codexproto\n\n")
	b.WriteString("import (\n\t\"context\"\n\t\"encoding/json\"\n)\n\n")

	b.WriteString("// ClientRequests exposes typed JSON-RPC calls supported by the app-server.\n")
	b.WriteString("type ClientRequests interface {\n")
	for _, method := range methods {
		if method.ParamsType == "" {
			fmt.Fprintf(&b, "\t%s(ctx context.Context) (*%s, error)\n", methodName(method.Method), method.ResponseType)
		} else {
			fmt.Fprintf(&b, "\t%s(ctx context.Context, params %s) (*%s, error)\n", methodName(method.Method), paramsType(method.ParamsType), method.ResponseType)
		}
	}
	b.WriteString("}\n\n")

	b.WriteString("// Client implements ClientRequests using JSON-RPC.\n")
	for _, method := range methods {
		if method.ParamsType == "" {
			fmt.Fprintf(&b, "func (c *Client) %s(ctx context.Context) (*%s, error) {\n", methodName(method.Method), method.ResponseType)
		} else {
			fmt.Fprintf(&b, "func (c *Client) %s(ctx context.Context, params %s) (*%s, error) {\n", methodName(method.Method), paramsType(method.ParamsType), method.ResponseType)
		}
		b.WriteString("\tvar result ")
		b.WriteString(method.ResponseType)
		b.WriteString("\n")
		if method.ParamsType == "" {
			fmt.Fprintf(&b, "\tif err := c.Call(ctx, %q, nil, &result); err != nil {\n", method.Method)
		} else {
			fmt.Fprintf(&b, "\tif err := c.Call(ctx, %q, params, &result); err != nil {\n", method.Method)
		}
		b.WriteString("\t\treturn nil, err\n\t}\n\treturn &result, nil\n}\n\n")
	}

	b.WriteString("// BuildClientRequest builds a JSON-RPC request for a client method.\n")
	b.WriteString("func BuildClientRequest(method string, params any, id RequestID) (JSONRPCRequest, error) {\n")
	b.WriteString("\tvar raw json.RawMessage\n\tif params != nil {\n\t\tdata, err := json.Marshal(params)\n\t\tif err != nil {\n\t\t\treturn JSONRPCRequest{}, err\n\t\t}\n\t\traw = data\n\t}\n\treturn JSONRPCRequest{ID: id, Method: method, Params: raw}, nil\n}\n")

	return []byte(b.String())
}

func renderServerRequests(methods []rpcMethod, codexCommit string) []byte {
	var b strings.Builder
	b.WriteString(generatedHeader(codexCommit))
	b.WriteString("package codexproto\n\n")
	b.WriteString("import (\n\t\"context\"\n\t\"encoding/json\"\n\t\"fmt\"\n)\n\n")

	b.WriteString("// ServerRequestHandler handles requests initiated by the app-server.\n")
	b.WriteString("type ServerRequestHandler interface {\n")
	for _, method := range methods {
		if method.ParamsType == "" {
			fmt.Fprintf(&b, "\t%s(ctx context.Context) (*%s, error)\n", methodName(method.Method), method.ResponseType)
		} else {
			fmt.Fprintf(&b, "\t%s(ctx context.Context, params %s) (*%s, error)\n", methodName(method.Method), paramsType(method.ParamsType), method.ResponseType)
		}
	}
	b.WriteString("}\n\n")

	b.WriteString("// ParsedServerRequest represents a typed server request envelope.\n")
	b.WriteString("type ParsedServerRequest struct {\n\tMethod string\n\tParams any\n\tRaw json.RawMessage\n}\n\n")

	b.WriteString("type serverRequestParser func(json.RawMessage) (ParsedServerRequest, error)\n\n")
	b.WriteString("var serverRequestParsers = map[string]serverRequestParser{\n")
	for _, method := range methods {
		fmt.Fprintf(&b, "\t%q: func(params json.RawMessage) (ParsedServerRequest, error) {\n", method.Method)
		if method.ParamsType == "" {
			fmt.Fprintf(&b, "\t\treturn ParsedServerRequest{Method: %q, Params: nil, Raw: params}, nil\n", method.Method)
		} else {
			b.WriteString("\t\tvar payload ")
			b.WriteString(method.ParamsType)
			b.WriteString("\n")
			b.WriteString("\t\tif len(params) > 0 {\n\t\t\tif err := json.Unmarshal(params, &payload); err != nil {\n\t\t\t\treturn ParsedServerRequest{Method: ")
			fmt.Fprintf(&b, "%q", method.Method)
			b.WriteString(", Raw: params}, err\n\t\t\t}\n\t\t}\n")
			b.WriteString("\t\treturn ParsedServerRequest{Method: ")
			fmt.Fprintf(&b, "%q", method.Method)
			b.WriteString(", Params: payload, Raw: params}, nil\n")
		}
		b.WriteString("\t},\n")
	}
	b.WriteString("}\n\n")

	b.WriteString("// ParseServerRequest parses a server request by method.\n")
	b.WriteString("func ParseServerRequest(method string, params json.RawMessage) (ParsedServerRequest, error) {\n")
	b.WriteString("\tif parser, ok := serverRequestParsers[method]; ok {\n\t\treturn parser(params)\n\t}\n")
	b.WriteString("\treturn ParsedServerRequest{Method: method, Raw: params}, nil\n}\n\n")

	b.WriteString("// IsKnownServerRequestMethod reports whether method is in the generated server request surface.\n")
	b.WriteString("func IsKnownServerRequestMethod(method string) bool {\n")
	b.WriteString("\t_, ok := serverRequestParsers[method]\n\treturn ok\n}\n\n")

	b.WriteString("func dispatchServerRequest(ctx context.Context, handler ServerRequestHandler, req JSONRPCRequest) (any, error) {\n")
	b.WriteString("\tswitch req.Method {\n")
	for _, method := range methods {
		fmt.Fprintf(&b, "\tcase %q:\n", method.Method)
		if method.ParamsType == "" {
			b.WriteString("\t\treturn handler.")
			b.WriteString(methodName(method.Method))
			b.WriteString("(ctx)\n")
		} else {
			b.WriteString("\t\tvar params ")
			b.WriteString(method.ParamsType)
			b.WriteString("\n")
			b.WriteString("\t\tif len(req.Params) > 0 {\n\t\t\tif err := json.Unmarshal(req.Params, &params); err != nil {\n\t\t\t\treturn nil, err\n\t\t\t}\n\t\t}\n")
			b.WriteString("\t\treturn handler.")
			b.WriteString(methodName(method.Method))
			b.WriteString("(ctx, params)\n")
		}
	}
	b.WriteString("\tdefault:\n\t\treturn nil, fmt.Errorf(\"unsupported server request %q\", req.Method)\n\t}\n}\n")

	return []byte(b.String())
}

func renderNotifications(notifications []rpcNotification, codexCommit string) []byte {
	var b strings.Builder
	b.WriteString(generatedHeader(codexCommit))
	b.WriteString("package codexproto\n\n")
	b.WriteString("import \"encoding/json\"\n\n")

	b.WriteString("// Notification represents a typed server notification.\n")
	b.WriteString("type Notification struct {\n\tMethod string\n\tParams any\n\tRaw json.RawMessage\n}\n\n")

	b.WriteString("type notificationParser func(json.RawMessage) (Notification, error)\n\n")
	b.WriteString("var notificationParsers = map[string]notificationParser{\n")
	for _, notification := range notifications {
		fmt.Fprintf(&b, "\t%q: func(params json.RawMessage) (Notification, error) {\n", notification.Method)
		if notification.ParamsType == "" {
			fmt.Fprintf(&b, "\t\treturn Notification{Method: %q, Params: nil, Raw: params}, nil\n", notification.Method)
		} else {
			b.WriteString("\t\tvar payload ")
			b.WriteString(notification.ParamsType)
			b.WriteString("\n")
			b.WriteString("\t\tif len(params) > 0 {\n\t\t\tif err := json.Unmarshal(params, &payload); err != nil {\n\t\t\t\treturn Notification{Method: ")
			fmt.Fprintf(&b, "%q", notification.Method)
			b.WriteString(", Raw: params}, err\n\t\t\t}\n\t\t}\n")
			b.WriteString("\t\treturn Notification{Method: ")
			fmt.Fprintf(&b, "%q", notification.Method)
			b.WriteString(", Params: payload, Raw: params}, nil\n")
		}
		b.WriteString("\t},\n")
	}
	b.WriteString("}\n\n")

	b.WriteString("// ParseServerNotification parses a server notification by method.\n")
	b.WriteString("func ParseServerNotification(method string, params json.RawMessage) (Notification, error) {\n")
	b.WriteString("\tif handler, ok := notificationParsers[method]; ok {\n\t\treturn handler(params)\n\t}\n")
	b.WriteString("\treturn Notification{Method: method, Raw: params}, nil\n}\n")

	b.WriteString("\n// IsKnownServerNotificationMethod reports whether method is in the generated server notification surface.\n")
	b.WriteString("func IsKnownServerNotificationMethod(method string) bool {\n")
	b.WriteString("\t_, ok := notificationParsers[method]\n\treturn ok\n}\n")

	return []byte(b.String())
}


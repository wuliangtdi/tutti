export function renderCliGoDefaults(specification) {
  return formatGo(`package defaults

// Code generated by tools/scripts/generate-defaults.mjs. DO NOT EDIT.

var generatedDefaults = generatedDefaultsSpec{
\tState: generatedStateDefaults{
\t\tProductionDirName:    ${goString(specification.state.productionDirName)},
\t\tDevelopmentDirName:   ${goString(specification.state.developmentDirName)},
\t\tRunDirName:           ${goString(specification.state.runDirName)},
\t\tListenerInfoFileName: ${goString(specification.state.listenerInfoFileName)},
\t},
}
`);
}

export async function renderTSDefaults(specification) {
  return await format(
    `export const generatedDefaults = ${JSON.stringify(specification, null, 2)} as const;\n`,
    {
      ...prettierConfig,
      parser: "typescript"
    }
  );
}

function goString(value) {
  return JSON.stringify(value);
}

function formatGo(content) {
  return execFileSync("gofmt", {
    input: content,
    encoding: "utf8"
  });
}

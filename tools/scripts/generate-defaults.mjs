import { execFileSync } from "node:child_process";
import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";
import { format } from "prettier";

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(__dirname, "../..");
const defaultsPath = resolve(repoRoot, "config/tutti.defaults.json");
const goOutputPath = resolve(
  repoRoot,
  "services/tuttid/types/defaults_generated.go"
);
const cliGoOutputPath = resolve(
  repoRoot,
  "apps/cli/internal/defaults/defaults_generated.go"
);
const tsOutputPath = resolve(
  repoRoot,
  "apps/desktop/src/main/generated/defaults.ts"
);
const prettierConfigPath = resolve(
  repoRoot,
  "packages/configs/prettier/base.mjs"
);
const checkOnly = process.argv.includes("--check");

const { default: prettierConfig } = await import(
  pathToFileURL(prettierConfigPath).href
);

if (isMainModule()) {
  const spec = JSON.parse(readFileSync(defaultsPath, "utf8"));

  writeGeneratedFile(goOutputPath, renderGoDefaults(spec));
  writeGeneratedFile(cliGoOutputPath, renderCliGoDefaults(spec));
  writeGeneratedFile(tsOutputPath, await renderTSDefaults(spec));
}

function isMainModule() {
  return Boolean(
    process.argv[1] &&
    resolve(process.argv[1]) === fileURLToPath(import.meta.url)
  );
}

function writeGeneratedFile(targetPath, content) {
  mkdirSync(dirname(targetPath), { recursive: true });

  if (checkOnly) {
    const existing = readFileSync(targetPath, "utf8");
    if (existing !== content) {
      throw new Error(`Generated defaults are out of date: ${targetPath}`);
    }
    return;
  }

  writeFileSync(targetPath, content, "utf8");
}

